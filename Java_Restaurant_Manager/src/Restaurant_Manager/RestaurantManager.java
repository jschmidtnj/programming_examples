    private void jcmbpaymentmethodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcmbpaymentmethodActionPerformed
        // TODO add your handling code here:
        PaymentMethod = (String)jcmbpaymentmethod.getSelectedItem();
        if(PaymentMethod == "Select"){
            requestFocusInWindow();
            jtxtpaymentmethod.setText("n/a");
            jtxtpaymentmethod.setEnabled(false);
            labelChange.setText("");
            labelTax.setText("7%");
            labelSubtotal.setText("");
            labelTotal.setText("");
        }
        else if(PaymentMethod == "Cash"){
            jtxtpaymentmethod.requestFocus();
            jtxtpaymentmethod.setText("0");
            jtxtpaymentmethod.setEnabled(true);
            labelChange.setText("click total");
            labelTax.setText("7%");
            labelSubtotal.setText("");
            labelTotal.setText("");
        }
        else{
            jtxtpaymentmethod.setEnabled(false);
            requestFocusInWindow();
            labelChange.setText("n/a");
            jtxtpaymentmethod.setText("n/a");
        }
    }//GEN-LAST:event_jcmbpaymentmethodActionPerformed

